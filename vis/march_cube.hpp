template<typename VectorType>
struct CompareVector
{
    bool operator()(const VectorType& v1,const VectorType v2) const
    {
        if (v1[0] != v2[0])
            return v1[0] < v2[0];
        if (v1[1] != v2[1])
            return v1[1] < v2[1];
        return v1[2] < v2[2];
    }
};

template<typename value_type>
struct GridCell
{
    tipl::vector<3> corner[8];
    value_type value[8];
};


class march_cube
{

public:
    std::map<tipl::vector<3>,unsigned int> points_map;
    std::vector<tipl::vector<3> > point_list;
    std::vector<tipl::vector<3> > normal_list;
    std::vector<tipl::vector<3,unsigned int> > tri_list;
    std::vector<unsigned int> sorted_index;
    size_t mesh_count = 0;
    size_t indices_count = 0;
private:
    tipl::vector<3> vertlist[12];
    unsigned int w,wh;
    size_t offset[7];
private:

    size_t getIndex(int index)
    {
        auto iter = points_map.find(vertlist[index]);
        if (iter == points_map.end())
        {
            unsigned int result = uint32_t(point_list.size());
            point_list.push_back(vertlist[index]);
            points_map[vertlist[index]] = result;
            return result;
        }
        else
            return iter->second;
    }


    /*
       Linearly interpolate the position where an isosurface cuts
       an edge between two vertices, each with their own scalar value
    */
    inline bool equal(float v1,float v2)
    {
        return std::fabs(v1-v2) < 1.0e-5f;
    }
    inline bool equal(double v1,double v2)
    {
        return std::abs(v1-v2) < 1.0e-5;
    }
    template<typename T>
    inline bool equal(T v1,T v2)
    {
        return v1 == v2;
    }
    template<typename value_type>
    void VertexInterp(unsigned int vert_index,value_type isolevel,tipl::vector<3> p1,tipl::vector<3> p2,value_type valp1,value_type valp2)
    {
        if (equal(isolevel,valp1))
        {
            vertlist[vert_index] = p1;
            return;
        }
        if (equal(isolevel,valp2))
        {
            vertlist[vert_index] = p2;
            return;
        }
        if (equal(valp1,valp2))
        {
            vertlist[vert_index] = p1;
            return;
        }
        float mu = (float(isolevel) - float(valp1)) / (float(valp2) - float(valp1));
        p2 *= mu;
        p2 += p1*(1.0f-mu);
        vertlist[vert_index] = p2;
    }

    void get_normal(void)
    {
        normal_list.clear();
        normal_list.resize(point_list.size());
        tipl::vector<3> n;
        tipl::par_for (tri_list.size(),[&](unsigned int index)
        {
            unsigned int p1 = tri_list[index][0];
            unsigned int p2 = tri_list[index][1];
            unsigned int p3 = tri_list[index][2];
            n = (point_list[p1] - point_list[p2]).cross_product(point_list[p2] - point_list[p3]);
            n.normalize();
            normal_list[p1] += n;
            normal_list[p2] += n;
            normal_list[p3] += n;
        },std::min<int>(8,std::thread::hardware_concurrency()));
        tipl::par_for (normal_list.size(),[&](unsigned int index)
        {
            normal_list[index].normalize();
        },std::min<int>(4,std::thread::hardware_concurrency()));
    }
    void get_sorted_indices(void)
    {
        sorted_index.resize(6*(indices_count));// 6 directions
        tipl::par_for(3,[&](size_t view_index)
        {
            std::vector<std::pair<float,unsigned int> > index_weighting(mesh_count);
            for (size_t index = 0;index < mesh_count;++index)
                index_weighting[index] =
                std::make_pair(point_list[tri_list[index][0]][view_index],index);

            std::sort(index_weighting.begin(),index_weighting.end());

            auto indices = sorted_index.begin() + view_index*indices_count;
            auto rindices = sorted_index.begin() + (view_index+4)*indices_count-3;
            for (size_t index = 0;index < mesh_count;++index,indices += 3,rindices -= 3)
            {
                auto new_index = index_weighting[index].second;
                rindices[0] = indices[0] = tri_list[new_index][0];
                rindices[1] = indices[1] = tri_list[new_index][1];
                rindices[2] = indices[2] = tri_list[new_index][2];
            }
        });
    }
    template<typename ImageType,typename ValueType>
    void addCube(const ImageType& source_image,const pixel_index<3>& point,ValueType isolevel)
    {
        using value_type = typename ImageType::value_type;
        GridCell<value_type> cell;
        cell.corner[0] = tipl::vector<3>(point[0],point[1],point[2]);
        cell.corner[1] = tipl::vector<3>(point[0]+1,point[1],point[2]);
        cell.corner[2] = tipl::vector<3>(point[0]+1,point[1]+1,point[2]);
        cell.corner[3] = tipl::vector<3>(point[0],point[1]+1,point[2]);
        cell.corner[4] = tipl::vector<3>(point[0],point[1],point[2]+1);
        cell.corner[5] = tipl::vector<3>(point[0]+1,point[1],point[2]+1);
        cell.corner[6] = tipl::vector<3>(point[0]+1,point[1]+1,point[2]+1);
        cell.corner[7] = tipl::vector<3>(point[0],point[1]+1,point[2]+1);
        cell.value[0] = source_image[point.index()];
        cell.value[1] = source_image[point.index()+offset[0]];
        cell.value[2] = source_image[point.index()+offset[1]];
        cell.value[3] = source_image[point.index()+offset[2]];
        cell.value[4] = source_image[point.index()+offset[3]];
        cell.value[5] = source_image[point.index()+offset[4]];
        cell.value[6] = source_image[point.index()+offset[5]];
        cell.value[7] = source_image[point.index()+offset[6]];
        /** Polygonise
        Given a grid cell and an isolevel, calculate the triangular
        facets required to represent the isosurface through the cell.
        Return the number of triangular facets, the array "triangles"
        will be loaded up with the vertices at most 5 triangular facets.
        0 will be returned if the grid cell is either totally above
        of totally below the isolevel.
        */
        {
            int cubeindex = 0;
            /*
              Determine the index into the edge table which
              tells us which vertices are inside of the surface
            */
            for (unsigned int index = 0;index < 8;++index)
                if (cell.value[index] <= isolevel)
                    cubeindex |= (1 << index);

            /* Cube is entirely in/out of the surface */
            if (MarchCubeData::edgeTable[cubeindex] == 0)
                return;

            /* Find the vertices where the surface intersects the cube */
            for (unsigned int index = 0;index < 12;++index)
                if (MarchCubeData::edgeTable[cubeindex] & (1 << index))
                    VertexInterp(index,isolevel,
                                 cell.corner[MarchCubeData::intersectTable[index][0]],
                                 cell.corner[MarchCubeData::intersectTable[index][1]],
                                 ValueType(cell.value[MarchCubeData::intersectTable[index][0]]),
                                 ValueType(cell.value[MarchCubeData::intersectTable[index][1]]));

            /* Create the triangle */
            for (unsigned int i=0;MarchCubeData::triTable[cubeindex][i]!=-1;i+=3)
                tri_list.push_back(
                    tipl::vector<3,unsigned int>(
                        getIndex(MarchCubeData::triTable[cubeindex][i  ]),
                        getIndex(MarchCubeData::triTable[cubeindex][i+1]),
                        getIndex(MarchCubeData::triTable[cubeindex][i+2])));

        }
    }
public:
    template<typename ImageType,typename ValueType>
    march_cube(ImageType& source_image,ValueType isolevel):
        w(source_image.shape()[0]),
        wh(uint32_t(source_image.shape().plane_size()))
    {
        offset[0] = 1;
        offset[1] = 1+w;
        offset[2] = w;
        offset[3] = wh;
        offset[4] = 1+wh;
        offset[5] = 1+w+wh;
        offset[6] = w+wh;

        // get all the edge cubes
        size_t image_size = source_image.size()-offset[5]; // make sure the image index will not out of bound
        for (size_t index = 0;index < image_size;++index)
        {
            bool greater = source_image[index] <= isolevel;
            for (auto shift : offset)
                if(greater ^ (source_image[index+shift] <= isolevel))
                {
                    addCube(source_image,tipl::pixel_index<3>(index,source_image.shape()),isolevel);
                    break;
                }
        }
        mesh_count = tri_list.size();
        indices_count = mesh_count*3;
        if(!mesh_count)
            return;
        get_normal();
        get_sorted_indices();
    }
};



